function Sidebar({ activeTab, setActiveTab }) {
  const [isOpen, setIsOpen] = useState(() => {
    if (window.innerWidth < 768) return false;
    const saved = localStorage.getItem('sidebarOpen');
    return saved !== null ? JSON.parse(saved) : true;
  });
  const [isFullscreen, setIsFullscreen] = useState(false);
  const tabs = ['NU', 'DETALJERAT', 'PROGNOS', 'INSTÄLLNINGAR'];

  useEffect(() => {
    const handleFullscreenChange = () => {
      setIsFullscreen(!!document.fullscreenElement);
    };
    document.addEventListener('fullscreenchange', handleFullscreenChange);
    return () => document.removeEventListener('fullscreenchange', handleFullscreenChange);
  }, []);

  const toggleSidebar = () => {
    const newState = !isOpen;
    setIsOpen(newState);
    if (window.innerWidth >= 768) {
      localStorage.setItem('sidebarOpen', JSON.stringify(newState));
    }
  };

  const toggleFullScreen = () => {
    if (!document.fullscreenElement) {
      document.documentElement.requestFullscreen().catch(err => {
        console.warn(`Kunde inte starta fullskärm: ${err.message}`);
      });
    } else {
      if (document.exitFullscreen) {
        document.exitFullscreen();
      }
    }
  };

  return (
    <div className={`sidebar ${isOpen ? 'open' : 'closed'}`}>
      <button className="toggle-btn" onClick={toggleSidebar} title={isOpen ? 'Dölj meny' : 'Visa meny'}>
        <span className="icon">{menuIcon}</span>
        <span className="text">{isOpen ? 'DÖLJ' : ''}</span>
      </button>
      <div className="sidebar-links">
        {tabs.map(tab => (
          <button
            key={tab}
            className={`sidebar-btn ${activeTab === tab ? 'active' : ''}`}
            onClick={() => {
              setActiveTab(tab);
            }}
            title={tab}
          >
            <span className="icon">{icons[tab]}</span>
            <span className="text">{tab}</span>
          </button>
        ))}
        <button
          className="sidebar-btn fullscreen-btn"
          onClick={toggleFullScreen}
          title={isFullscreen ? 'Stäng fullskärm' : 'Fullskärm'}
        >
          <span className="icon">{icons.FULLSCREEN}</span>
          <span className="text">{isFullscreen ? 'STÄNG' : 'FULLSKÄRM'}</span>
        </button>
      </div>
    </div>
  );
}

export default Sidebar;
